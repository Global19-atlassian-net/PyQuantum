# ---------------------------------------------------------------------------------------------------------------------
# PyQuantum.Tools
from PyQuantum.Tools.Assert import Assert
from PyQuantum.Tools.Print import cprint
from PyQuantum.Tools.Hz import to_Hz
# ---------------------------------------------------------------------------------------------------------------------



class Cavity:
    # -----------------------------------------------------------------------------------------------------------------
    # ---------------------------------------------------- INIT -------------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------
    def __init__(self, wc, wa, g, n_atoms, n_levels=2):
        Assert(isinstance(wc, (int, float)),
               'wc is not numeric')
        Assert(wc > 0, 'wc <= 0')

        self.wc = wc

        Assert(isinstance(wa, (int, float)),
               'wa is not numeric')
        Assert(wa > 0, 'wa <= 0')
        self.wa = wa

        Assert(isinstance(g, (int, float)), 'g is not numeric')
        Assert(g > 0, 'g <= 0')
        self.g = g

        Assert(isinstance(n_atoms, int),
               'n_atoms is not integer')
        Assert(n_atoms > 0, 'n_atoms <= 0')
        self.n_atoms = n_atoms

        Assert(isinstance(n_levels, int),
               'n_atoms is not integer')
        Assert(n_levels > 1, 'n_levels <= 2')
        self.n_levels = n_levels
    # -----------------------------------------------------------------------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------



    # -----------------------------------------------------------------------------------------------------------------
    # ---------------------------------------------------- WC_INFO ----------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------
    def wc_info(self):
        cprint('wc: ', 'yellow', end='')

        print(to_Hz(self.wc))

        print()
    # -----------------------------------------------------------------------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------



    # -----------------------------------------------------------------------------------------------------------------
    # ---------------------------------------------------- WA_INFO ----------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------
    def wa_info(self):
        cprint('wa: ', 'yellow', end='')

        print(to_Hz(self.wa))

        print()
    # -----------------------------------------------------------------------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------



    # -----------------------------------------------------------------------------------------------------------------
    # ---------------------------------------------------- G_INFO -----------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------
    def g_info(self):
        cprint(' g: ', 'yellow', end='')

        print(to_Hz(self.g))

        print()
    # -----------------------------------------------------------------------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------



    # -----------------------------------------------------------------------------------------------------------------
    # ---------------------------------------------------- N_ATOMS_INFO -----------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------
    def n_atoms_info(self):
        cprint(' n_atoms: ', 'yellow', end='')

        print(self.n_atoms)

        print()
    # -----------------------------------------------------------------------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------



    # -----------------------------------------------------------------------------------------------------------------
    # ---------------------------------------------------- N_LEVELS_INFO ----------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------
    def n_levels_info(self):
        cprint(' n_levels: ', 'yellow', end='')

        print(self.n_levels)

        print()
    # -----------------------------------------------------------------------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------



    # -----------------------------------------------------------------------------------------------------------------
    # ---------------------------------------------------- INFO -------------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------
    def info(self, title='Cavity:'):
        cprint(title, 'green')

        print()

        self.wc_info()
        self.wa_info()
        self.g_info()
        self.n_atoms_info()
        self.n_levels_info()
    # -----------------------------------------------------------------------------------------------------------------
    # -----------------------------------------------------------------------------------------------------------------

# =====================================================================================================================
