# =====================================================================================================================
# PyQuantum.Tools
from PyQuantum.Tools.Assert import Assert
from PyQuantum.Tools.Print import cprint
from PyQuantum.Tools.Hz import to_Hz
# =====================================================================================================================


# =====================================================================================================================
class Cavity:
    # -----------------------------------------------------------------------------------------------------------------
    def __init__(self, wc, wa, g, n_atoms, n_levels=2):
        Assert(isinstance(wc, (int, float)),
               'wc is not numeric')
        Assert(wc > 0, 'wc <= 0')

        self.wc = wc

        Assert(isinstance(wa, (int, float)), 'wa is not numeric')
        Assert(wa > 0, 'wa <= 0')
        self.wa = wa

        Assert(isinstance(g, (int, float)), 'g is not numeric')
        Assert(g > 0, 'g <= 0')
        self.g = g

        Assert(isinstance(n_atoms, int),
               'n_atoms is not integer')
        Assert(n_atoms in [0, 1], 'n_atoms not in [0, 1]')
        self.n_atoms = n_atoms

        Assert(isinstance(n_levels, int), 'n_atoms is not integer')
        Assert(n_levels > 1, 'n_levels <= 2')
        self.n_levels = n_levels
    # -----------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------
    def wc_info(self):
        cprint('wc = ', 'yellow', attrs=['bold'], end='')

        print(to_Hz(self.wc))
    # -----------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------
    def wa_info(self):
        cprint('wa = ', 'yellow', attrs=['bold'], end='')

        print(to_Hz(self.wa))
    # -----------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------
    def g_info(self):
        cprint(' g = ', 'yellow', attrs=['bold'], end='')

        print(to_Hz(self.g))

        print()
    # -----------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------
    def n_atoms_info(self):
        cprint(' n_atoms: ', 'yellow', attrs=['bold'], end='')

        print(self.n_atoms)
    # -----------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------
    def n_levels_info(self):
        cprint('n_levels: ', 'yellow', attrs=['bold'], end='')

        print(self.n_levels)

        print()
    # -----------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------
    def info(self, title='Cavity:'):
        cprint(title, 'green', attrs=['bold'])

        print()

        self.wc_info()
        self.wa_info()
        self.g_info()
        self.n_atoms_info()
        self.n_levels_info()
    # -----------------------------------------------------------------------------------------------------------------

# =====================================================================================================================
